package Week11;

public class WhileLoop_01 {
    public static void main(String[] args) {
        // ค่าเริ่มต้น
        // เงื่อนไข
        // upadate รอบ  
        int count = 1; //ค่าเริ่มต้น
        while (count <= 10) { //เงื่อนไข
            System.out.println("รอบที่ "+count+"java"); //ค่าที่ต้องการแสดง
            count++;    //upadate รอบ 
            
        }
    }
    
}
